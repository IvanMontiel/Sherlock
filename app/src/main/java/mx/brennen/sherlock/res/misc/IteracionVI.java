package mx.brennen.sherlock.res.misc;

public class IteracionVI {

    int Iteracion;
    double an, bn, pn, fn;

    public int getIteracion() {
        return Iteracion;
    }

    public void setIteracion(int iteracion) {
        Iteracion = iteracion;
    }

    public double getAn() {
        return an;
    }

    public void setAn(double an) {
        this.an = an;
    }

    public double getBn() {
        return bn;
    }

    public void setBn(double bn) {
        this.bn = bn;
    }

    public double getPn() {
        return pn;
    }

    public void setPn(double pn) {
        this.pn = pn;
    }

    public double getFn() {
        return fn;
    }

    public void setFn(double fn) {
        this.fn = fn;
    }
}
